let animales = ["pato", "caballo"];
console.log(animales);

// Para poder accedder al primer elemento tenemos que agregar parentesis cuadrado como aqui:
console.log(animales[0]);
console.log(animales[1]);

// Para agregar un nuevo elemento con el siguiente indice se hace de esta manera:
animales [2] = "conejo";
console.log(animales);
