// Operadores relacionales
let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Operadores de igualdad
console.log(a == 10);
console.log(a != 10); // ! significa distinto
console.log(a === '10');
console.log(a !== '10');

//los simbolos que mas se utilizan actualmente y para evitar errores son:   ===  !==    por buenas practicas 